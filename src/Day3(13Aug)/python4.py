#LIFO (last in fast out), for this use pop() function which will give the last element first & count
l=[1,20,30,30,40,30,20]
print(l.pop())
print(l.count(30))

#max & min & sum
l=[1,20,30,30,40,30,20]
print(max(l))
print(min(l))
print(sum(l))

